    /**
     * @return Imposto[] Returns an array of imposto objects
     */
    public function getTaxeForPlantao($plantoes)
    {
        $qb = $this->createQueryBuilder('taxe');

        $qb
            ->select('
                taxe.type AS type,
                taxe.value AS value,
                plantao.value AS pvalue
            ')
            ->innerJoin(Plantao::class,'plantao','WITH','plantao.id = taxe.plantao_id')
            ->where('taxe.plantao_id IN(:plantoes)')
            ->setParameter('plantoes',$plantoes)
        ;

        return $qb->getQuery()->getResult();
    }
